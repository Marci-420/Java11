public class Jaror {

    private int h, min, mp;
    private String jarmu;

    public Jaror(int h, int min, int mp, String jarmu) {
        this.h = h;
        this.min = min;
        this.mp = mp;
        this.jarmu = jarmu;
    }

    public int getH() {
        return h;
    }

    public void setH(int h) {
        this.h = h;
    }

    public int getMin() {
        return min;
    }

    public void setMin(int min) {
        this.min = min;
    }

    public int getMp() {
        return mp;
    }

    public void setMp(int mp) {
        this.mp = mp;
    }

    public String getJarmu() {
        return jarmu;
    }

    public void setJarmu(String jarmu) {
        this.jarmu = jarmu;
    }
}
